interface I18nState {
  lang: Language;
  setLanguage: (lang: Language) => void;
  t: (key: string) => string;
}

export const useI18nStore = create<I18nState>((set, get) => ({
  lang: 'ID',
  setLanguage: (lang) => set({ lang }),
  t: (key) => {
    const currentLang = get().lang;
    return translations[currentLang][key] || key;
  }
}));
